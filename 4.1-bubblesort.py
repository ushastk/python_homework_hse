import random
import time
import math

def BubbleSort(arr):
    n = len(arr) - 1
    for i in range(n, 0, -1):
        for j in range(i):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
    return arr

